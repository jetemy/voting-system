    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        if (!"".equals(txtEmpId.getText())) {
            try {

                String sql = "SELECT * FROM users WHERE Employee_ID=?";
                pstmt = con.prepareStatement(sql);
                pstmt.setString(1, txtEmpId.getText());
                rs = pstmt.executeQuery();
                if (!rs.isBeforeFirst()) {
                    lblNoResult.setText("No result found");
                    txtFname.setText(null);
                    txtSname.setText(null);
                    cmbUserType.setSelectedIndex(0);
                    btnsubmit.setEnabled(false);
                    btndelete.setEnabled(false);
                    lblPic.setIcon(null);

                    txtEmpId.requestFocus();
                } else {
                    while (rs.next()) {
                        btnsubmit.setEnabled(true);
                        btndelete.setEnabled(true);
                        lblNoResult.setText(null);
                        txtFname.setText(rs.getString("First_name"));
                        txtSname.setText(rs.getString("Second_name"));
                        cmbUserType.setSelectedItem(rs.getObject("User_Type"));
                        retriveImage();
                    }

                }
            } catch (java.lang.NullPointerException e) {
                JOptionPane.showMessageDialog(null, "No connection", "Connection Failed", JOptionPane.ERROR_MESSAGE);

            } catch (SQLException ex) {
                Logger.getLogger(Existing_User.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Employee ID is required", "Missing Data", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_btnsearchActionPerformed

