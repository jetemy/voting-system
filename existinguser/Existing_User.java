    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed
        if (!"".equals(txtEmpId.getText())) {
            if ((!"".equals(txtFname.getText())) && (!"".equals(txtSname.getText())) && (0!=cmbUserType.getSelectedIndex())) {
                try {

                    String sql = "UPDATE  users SET First_name='" + txtFname.getText() + "'"
                            + "Second_name='" + txtSname.getText().toUpperCase() + "'"
                            + "User_Type='" + cmbUserType.getSelectedItem() + "'"
                            + "WHERE Employee_ID='" + txtEmpId.getText().toUpperCase() + "'";
                    pstmt.execute(sql);
                    JOptionPane.showMessageDialog(null, "updated successfully", "Information", JOptionPane.INFORMATION_MESSAGE);
                } catch (java.lang.NullPointerException e) {
                    JOptionPane.showMessageDialog(null, "Connection Failed", "No connection", JOptionPane.ERROR_MESSAGE);
                } catch (com.mysql.jdbc.exceptions.jdbc4.MySQLIntegrityConstraintViolationException e) {
                    JOptionPane.showMessageDialog(null, "Duplicate Entry,ID number already exist", "", JOptionPane.ERROR_MESSAGE);
                } catch (SQLException ex) {
                    Logger.getLogger(Existing_User.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "All fields are required", "Missing Input", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Employee ID is required field", "Missing Input", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_btnsubmitActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed

        if (txtEmpId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Employee ID is required!", "Error", JOptionPane.ERROR_MESSAGE);
            txtEmpId.requestFocus();
        } else {

            int delete = JOptionPane.showConfirmDialog(null, "Do you really want to delete?", "Delete", JOptionPane.YES_NO_OPTION);
            if (delete == 0) {
                try {

                    String sql = "DELETE  FROM users WHERE Employee_ID='" + txtEmpId.getText() + "'";
                    pstmt = con.prepareStatement(sql);
                    pstmt.execute(sql);
                    JOptionPane.showMessageDialog(null, "Deleted");
                } catch (java.lang.NullPointerException e) {
                    JOptionPane.showMessageDialog(null, "No connection", "Connection Failed", JOptionPane.ERROR_MESSAGE);
                } catch (SQLException ex) {
                    Logger.getLogger(Existing_User.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
    }//GEN-LAST:event_btndeleteActionPerformed
    }
