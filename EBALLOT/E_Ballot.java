public void Confirmdetails() {
        if ((!"".equals(txtRegNo.getText())) && (!"".equals(txtPwd.getText()))) {
            try {
                String sqlConfirm = "SELECT * FROM register_voter WHERE Reg_No='" + txtRegNo.getText() + "' AND Password='" + txtPwd.getText() + "'";
                pstmt = con.prepareStatement(sqlConfirm);
                rs = pstmt.executeQuery();
                int count = 0;
                while (rs.next()) {
                    count++;
                }

                if (count < 1) {
                    JOptionPane.showMessageDialog(null, "Wrong Password or Registration Number", "ERROR", JOptionPane.ERROR_MESSAGE);

                } else {

                    ViewStatus();
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Unable to login because no connection is established");
            }
        } else {
            JOptionPane.showMessageDialog(null, "All fields are required", "ERROR", JOptionPane.WARNING_MESSAGE);
        }

    }
